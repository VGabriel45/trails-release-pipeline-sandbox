function sanitizeFileName(name) {
  return name.replace(/[^a-z0-9._-]+/gi, "-").replace(/^-|-$/g, "")
}

async function importTypeScriptModule(relativePath) {
  const absolutePath = path.join(repoRoot, relativePath)
  const source = await readFile(absolutePath, "utf8")
  const transpiled = ts.transpileModule(source, {
    compilerOptions: {
      jsx: ts.JsxEmit.ReactJSX,
      module: ts.ModuleKind.ESNext,
      target: ts.ScriptTarget.ES2023,
      verbatimModuleSyntax: false,
    },
    fileName: absolutePath,
  })

  const encoded = encodeURIComponent(
    `${transpiled.outputText}\n//# sourceURL=${absolutePath.replace(/\\/g, "/")}`,
  )
  return import(`data:text/javascript;charset=utf-8,${encoded}`)
}

function generatedHeader(sourceLabel) {
  return `// Generated by scripts/typecheck-snippets.mjs from ${sourceLabel}.\n// Do not edit this file; rerun pnpm typecheck:snippets.\n\n`
}

function extractInlineScript(htmlSnippet) {
  const matches = [
    ...htmlSnippet.matchAll(/<script\b[^>]*>([\s\S]*?)<\/script\b[^>]*>/gi),
  ]
  return matches.at(-1)?.[1]?.trim() ?? ""
}

function validateJavaScriptSyntax(source, label) {
  try {
    new Function(source)
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error)
    throw new Error(`Invalid JavaScript syntax in ${label}: ${message}`, {
      cause: error,
    })
  }
}

async function prepareProject(projectDir, tsconfigExtends, files) {
  const absoluteProjectDir = path.join(repoRoot, projectDir)
  const outputDir = path.join(absoluteProjectDir, ".snippet-typecheck")
  await rm(outputDir, { force: true, recursive: true })
  await mkdir(outputDir, { recursive: true })

  for (const file of files) {
    const absoluteFilePath = path.join(outputDir, file.name)
    await mkdir(path.dirname(absoluteFilePath), { recursive: true })
    await writeFile(
      absoluteFilePath,
      `${generatedHeader(file.source)}${file.content.trim()}\n`,
    )
  }

  await writeFile(
    path.join(outputDir, "tsconfig.json"),
    `${JSON.stringify(
      {
        extends: tsconfigExtends,
        compilerOptions: {
          noEmit: true,
          noUnusedLocals: false,
          noUnusedParameters: false,
        },
        include: ["./**/*.ts", "./**/*.tsx", "../src/**/*.d.ts"],
      },
      null,
      2,
    )}\n`,
  )

  return path.relative(repoRoot, path.join(outputDir, "tsconfig.json"))
}

async function runTsc(tsconfigPath) {
  await new Promise((resolve, reject) => {
    const child = spawn("pnpm", ["exec", "tsc", "-p", tsconfigPath], {
      cwd: repoRoot,
      stdio: "inherit",
    })
    child.on("error", reject)
    child.on("exit", (code) => {
      if (code === 0) {
        resolve()
      } else {
        reject(
          new Error(`tsc failed for ${tsconfigPath} with exit code ${code}`),
        )
      }
    })
  })
}

async function main() {
  const tsconfigs = []

  const { codeSnippets } = await importTypeScriptModule(
    "apps/trails-demo/src/routes/sdk-sandbox/codeSnippets.ts",
  )
  const { buildCodeSnippets } = await importTypeScriptModule(
    "apps/trails-demo/src/routes/widget-demo/components/codeSnippetBuilders.ts",
  )
  const trailsDemoFiles = Object.entries(codeSnippets).map(
    ([slug, snippet]) => ({
      name: `sdk-sandbox/${sanitizeFileName(slug)}.tsx`,
      source: `apps/trails-demo/src/routes/sdk-sandbox/codeSnippets.ts#${slug}`,
      content: snippet,
    }),
  )

  for (const [caseName, options] of widgetCases) {
    const snippets = buildCodeSnippets(options)
    trailsDemoFiles.push(
      {
        name: `widget-demo/${caseName}.react.tsx`,
        source: `CodeSnippet ${caseName} react tab`,
        content: snippets.react,
      },
      {
        name: `widget-demo/${caseName}.mode.tsx`,
        source: `CodeSnippet ${caseName} mode tab`,
        content: snippets.mode,
      },
      {
        name: `widget-demo/${caseName}.hook.tsx`,
        source: `CodeSnippet ${caseName} hook tab`,
        content: snippets.hook,
      },
    )

    const inlineScript = extractInlineScript(snippets.script)
    if (inlineScript) {
      validateJavaScriptSyntax(
        inlineScript,
        `CodeSnippet ${caseName} script tab`,
      )
    }
  }

  tsconfigs.push(
    await prepareProject(
      "apps/trails-demo",
      "../tsconfig.app.json",
      trailsDemoFiles,
    ),
  )

  const { hookDemoSnippets } = await importTypeScriptModule(
    "apps/examples/shared/src/components/hooks-sandbox/codeSnippets.ts",
  )
  const exampleSharedFiles = Object.entries(hookDemoSnippets).map(
    ([slug, snippet]) => ({
      name: `hooks-sandbox/${sanitizeFileName(slug)}.tsx`,
      source: `apps/examples/shared/src/components/hooks-sandbox/codeSnippets.ts#${slug}`,
      content: snippet,
    }),
  )

  tsconfigs.push(
    await prepareProject(
      "apps/examples/shared",
      "../tsconfig.json",
      exampleSharedFiles,
    ),
  )

  for (const tsconfigPath of tsconfigs) {
    console.log(`\n> tsc -p ${tsconfigPath}`)
    await runTsc(tsconfigPath)
  }

  console.log("\nSnippet typecheck passed.")
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
